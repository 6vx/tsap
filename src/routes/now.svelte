    <h2>Now</h2>
    <p>I'm living in Calgary, Alberta, and working on personal projects while learning and hanging out with my friend Sophie.</p>
    <h3>Making Apps</h3>
    <p>I've had a game in my head for a long time, and every time I try to pop it out I end up learning something, realizing I'm doing everything wrong, and going back to start over.</p>
    <p>I think now my ability to "make whatever I want" has reached a point that it's sort of spilling out at a pace too quick for it to not coalesce into a standalone app (or three) one of these days.</p>
    <p>The journey has made me realize that it's not such an imposing thing to make any particular algorithm (it seems like 99% of being a developer is cutting and pasting strings together, sending them to and fro). Maintaining the mental scope of every moving part, and how changing pieces affects the whole, is much more difficult than actually implementing something.</p>
    <p>There's a thousand ways to skin every cat, and there are plenty of better and worse ways among those. And a curious, attention-deficit-disordered type like me can spend infinity time chasing down every option in search of the most efficient or the most succinct solution to a problem. Experimenting with them. Casting out some and remembering others. </p>
    <p>Anyway, eventually you're sitting on a server that is generating your game state every day, and you've gone through three or four implementations of the "front-end" of said game, and you come to an age old idiom...</p>
    <p><b><u>Done is better than good.</u></b></p>
